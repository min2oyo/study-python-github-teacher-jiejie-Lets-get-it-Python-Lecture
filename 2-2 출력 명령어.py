


# 연산자
print(9 + 6)	# 15			// 덧셈
print(9 - 6)	# 3				// 뺄셈
print(9 * 6)	# 54			// 곱셈
print(9 / 6)	# 1.5			// 나눗셈
print(9 // 6)	# 1				// 나눗셈 몫
print(9 % 6)	# 3				// 나눗셈 나머지
print(9 ** 6)	# 531441	// 제곱

# 연산자 연결
print(14 + 31)								# 45
print('파이썬' + '프로그래밍')	# 파이썬프로그래밍

print(14, 31)								# 14 31
print('파이썬', '프로그래밍')	# 파이썬 프로그래밍






